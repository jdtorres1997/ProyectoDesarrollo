    private void generarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarReporteActionPerformed
        // TODO add your handling code here:

        if (validarFechas()) {
            boolean guardo = false;
            java.util.Date fechaDesde = desde.getSelectedDate().getTime();
            java.util.Date fechaHasta = hasta.getSelectedDate().getTime();

            java.sql.Date desdeSql = new java.sql.Date(fechaDesde.getTime());
            java.sql.Date hastaSql = new java.sql.Date(fechaHasta.getTime());

            String rutaArchivo = ruta.getText();
            String nombre = nombreArchivo.getText();
            for (Enumeration<AbstractButton> buttons = buttonGroup1.getElements(); buttons.hasMoreElements();) {
                AbstractButton button = buttons.nextElement();
                if (button.isSelected()) {
                    if (button.equals(pagos)) {
                        guardo = controladorReportes.reportePagos(rutaArchivo, nombre, desdeSql, hastaSql);
                    } else if (button.equals(nopagos)) {
                        guardo = controladorReportes.reporteNoPagos(rutaArchivo, nombre, desdeSql, hastaSql);
                    } else if (button.equals(pagosynopagos)) {
                        guardo = controladorReportes.reportePagosYNoPagos(rutaArchivo, nombre, desdeSql, hastaSql);
                    }
                }
            }

            if (guardo) {
                JOptionPane.showMessageDialog(this, "El reporte se guardo correctamente", "", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Hubo un error al itentar crear el reporte, revise que el nombre del archivo y la ruta sean validas o que si el archivo ya existe que no este abierto", "", JOptionPane.ERROR_MESSAGE);
            }

        }
        

    }//GEN-LAST:event_generarReporteActionPerformed

